type MessageValue = string | ((params: Record<string, string | number>) => string);

export type MessageDictionary = Record<string, MessageValue>;

function interpolate(template: string, params: Record<string, string | number>) {
  return template.replace(/\{(\w+)\}/g, (_match, key: string) => {
    const value = params[key];
    return value === undefined ? `{${key}}` : String(value);
  });
}

function withFallback(base: MessageDictionary, overrides: Partial<MessageDictionary>): MessageDictionary {
  return {
    ...base,
    ...overrides,
  } as MessageDictionary;
}

