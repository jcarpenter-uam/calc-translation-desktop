export const MESSAGES: Record<UiLocale, MessageDictionary> = {
  en: englishMessages,
  es: spanishMessages,
  zh: chineseMessages,
  id: indonesianMessages,
};

export function formatMessage(
  dictionary: MessageDictionary,
  key: string,
  params: Record<string, string | number> = {},
) {
  const value = dictionary[key] || englishMessages[key] || key;

  if (typeof value === "function") {
    return value(params);
  }

  return interpolate(value, params);
}
